class WaterAndOxygen {
public:
	double maxDays(int, int, int, int);
};


double WaterAndOxygen::maxDays(int remainH2O, int remainO2, int costH2O, int costO2) {
	double r0 = remainH2O, c0 = costH2O, r1 = remainO2, c1 = costO2;
	if (r0 * c1 < r1 * c0)
		return r0 / c0;
	double A = r0 / 2.0 + r1;
	double B = c0 / 2.0 + c1;
	return A / B;
}



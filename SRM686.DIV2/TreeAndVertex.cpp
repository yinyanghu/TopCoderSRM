class TreeAndVertex {
public:
	int get(vector <int>);
};

const int maxN = 105;

int degree[maxN];

int TreeAndVertex::get(vector <int> tree) {
	memset(degree, 0, sizeof(degree));
	for (int i = 0; i < tree.size(); ++ i) {
		++ degree[i + 1];
		++ degree[tree[i]];
	}
	int ret = 0;
	for (int i = 0; i <= tree.size(); ++ i)
		ret = max(ret, degree[i]);
	return ret;
}



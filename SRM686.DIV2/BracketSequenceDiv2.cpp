class BracketSequenceDiv2 {
public:
	int count(string);
};

const int maxN = 105;
const int MOD = 1000000007;

int F[maxN][maxN];

int dp(const string &s, int x, int open) {
	if (F[x][open] != -1) return F[x][open];
	int ret = 0;
	if (open == 0) ++ ret;

	int y;
	for (y = x; y < s.size() && s[y] != '('; ++ y);
	if (y < s.size())
		ret = (ret + dp(s, y + 1, open + 1)) % MOD;


	if (open > 0) {
		for (y = x; y < s.size() && s[y] != ')'; ++ y);
		if (y < s.size())
			ret = (ret + dp(s, y + 1, open - 1)) % MOD;
	}

	F[x][open] = ret;
	return ret;
}

int BracketSequenceDiv2::count(string s) {
	memset(F, -1, sizeof(F));
	return dp(s, 0, 0) - 1;
}



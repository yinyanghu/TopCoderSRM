class UpDownNess {
public:
	int count(int, int);
};

long long f[51][5010];

const int MOD = 1000000007;

int UpDownNess::count(int N, int K) {
	memset(f, 0, sizeof(f));

	f[1][0] = 1;
	f[2][0] = 2;
	f[3][0] = 4; f[3][1] = 2;

	for (int i = 4; i <= N; ++ i) {
		for (int k = 0; k <= K; ++ k) {
			for (int j = 0; i - 1 - j >= 0; ++ j) {
				int p = j * (i - 1 - j);
				if (k >= p)
					f[i][k] = (f[i][k] + f[i - 1][k - p]) % MOD;
			}
		}
	}
	return (int)f[N][K];
}



class EllysTimeMachine {
public:
	string getTime(string);
};

string EllysTimeMachine::getTime(string time) {
	map<string, string> Hours;
	map<string, string> Minutes;
	Hours["12"] = "00";
	Hours["01"] = "05";
	Hours["02"] = "10";
	Hours["03"] = "15";
	Hours["04"] = "20";
	Hours["05"] = "25";
	Hours["06"] = "30";
	Hours["07"] = "35";
	Hours["08"] = "40";
	Hours["09"] = "45";
	Hours["10"] = "50";
	Hours["11"] = "55";

	Minutes["00"] = "12";
	Minutes["05"] = "01";
	Minutes["10"] = "02";
	Minutes["15"] = "03";
	Minutes["20"] = "04";
	Minutes["25"] = "05";
	Minutes["30"] = "06";
	Minutes["35"] = "07";
	Minutes["40"] = "08";
	Minutes["45"] = "09";
	Minutes["50"] = "10";
	Minutes["55"] = "11";

	string hour = time.substr(0, 2);
	string minute = time.substr(3, 2);

	return Minutes[minute] + ":" + Hours[hour];
}



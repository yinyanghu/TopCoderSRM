class ExploringNumbers {
public:
	int numberOfSteps(int);
};

bool isPrime(int x) {
	if (x == 1) return false;
	if (x == 2) return true;
	if ((x & 1) == 0) return false;
	for (int i = 3; i * i <= x; i += 2)
		if (x % i == 0) return false;
	return true;
}

int next(int x) {
	int ret = 0;
	for (; x; x /= 10) ret += (x % 10) * (x % 10);
	return ret;
}

int ExploringNumbers::numberOfSteps(int n) {
	unordered_set<int> S;
	int ret = 1;
	for (;!isPrime(n); n = next(n)) {
		if (S.find(n) != S.end()) return -1;
		++ ret; S.insert(n);
	}
	return ret;
}



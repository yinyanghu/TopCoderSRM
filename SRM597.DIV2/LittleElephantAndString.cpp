class LittleElephantAndString {
public:
	int getNumber(string, string);
};

int LittleElephantAndString::getNumber(string A, string B) {
    if (A == B) return 0;

    string AA = A;
    string BB = B;
    sort(AA.begin(), AA.end());
    sort(BB.begin(), BB.end());
    if (AA != BB)
        return -1;

    int j = B.size() - 1;
    for (int i = A.size() - 1; i >= 0; -- i)
        if (A[i] == B[j])
            -- j;
    return j + 1;
}


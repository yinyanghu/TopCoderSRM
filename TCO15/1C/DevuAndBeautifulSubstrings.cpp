class DevuAndBeautifulSubstrings {
public:
	long long countBeautifulSubstrings(int, int);
private:
	long long f[51][1300];
};

long long DevuAndBeautifulSubstrings::countBeautifulSubstrings(int n, int cnt) {
	memset(f, 0, sizeof(f));	
	f[1][1] = 1;
	for (int i = 2; i <= n; ++ i) {
		f[i][i] = 1;
		int total = i * (i + 1) / 2;
		f[i][total] = 1;
		for (int j = i + 1; j < total; ++ j) {
			for (int k = 1; k < i; ++ k) {
				int totalk = k * (k + 1) / 2;
				f[i][j] += f[i - k][j - totalk];
			}
		}
	}
	return f[n][cnt] * 2;
}



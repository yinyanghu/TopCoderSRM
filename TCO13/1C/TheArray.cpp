class TheArray {
public:
	int find(int, int, int, int);
};


int TheArray::find(int n, int d, int first, int last) {
	int ans = -100000000;
	for (int i = 0; i < n; ++ i)
	{
		int L = first + i * d;
		int R = last + (n - i - 1) * d;
		ans = max(ans, min(L, R));
	}
	return ans;
}


class Quorum {
public:
	int count(vector <int>, int);
};

int Quorum::count(vector <int> arr, int k) {
	sort(arr.begin(), arr.end());
	int ret = 0;
	for (int i = 0; i < k; ++ i)
		ret += arr[i];
	return ret;	
}



class ParenthesesDiv2Easy {
public:
	int getDepth(string);
};

int ParenthesesDiv2Easy::getDepth(string s) {
	//return depth(s, 0, s.size() - 1);
	int ret = 0;
	int count = 0;
	for (int i = 0; i < s.size(); ++ i) {
		if (s[i] == '(') ++ count; else -- count;
		ret = max(ret, count);
	}
	return ret;
}


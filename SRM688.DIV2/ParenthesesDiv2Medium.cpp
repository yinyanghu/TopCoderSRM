class ParenthesesDiv2Medium {
public:
	vector <int> correct(string);
};

int flag[60];

vector <int> ParenthesesDiv2Medium::correct(string s) {
	for (int i = 0; i < s.size(); ++ i)
		flag[i] = i;
	while (1) {
		size_t pos = s.find("()");
		if (pos == string::npos) break;
		for (int i = pos; i + 2 < s.size(); ++ i)
			flag[i] = flag[i + 2];
		s.erase(pos, 2);
	}
	cout << s << endl;
	vector<int> ret;
	if (s.empty()) return ret;
	int i;
	for (i = 0; i < s.size() - 1; i += 2) {
		if (s[i] == ')' && s[i + 1] == ')')
			ret.push_back(flag[i]);
		else if (s[i] == ')' && s[i + 1] == '(') break;
	}
	int j;
	for (j = s.size() - 1; j >= 1; j -= 2) {
		if (s[j] == '(' && s[j - 1] == '(')
			ret.push_back(flag[j]);
		else if (s[j] == '(' && s[j - 1] == ')') break;
	}
	if (j != 0 && i != s.size()) {
		ret.push_back(flag[i]);
		ret.push_back(flag[j]);
	}
	sort(ret.begin(), ret.end());
	return ret;
}


